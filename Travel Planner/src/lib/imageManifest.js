export function getCategoryFallback(category) {
  if (!category) {
    return IMAGE_FALLBACKS.scenic;
  }

  const normalizedCategory = String(category).trim().toLowerCase();
  return CATEGORY_FALLBACKS[normalizedCategory] ?? IMAGE_FALLBACKS.scenic;
}

export function getManifestImageForQuery(query, { category } = {}) {
  const normalizedQuery = typeof query === "string" ? query.toLowerCase() : "";
  const match = IMAGE_RULES.find(({ keywords }) =>
    keywords.some((keyword) => normalizedQuery.includes(keyword))
  );

  if (match?.image) {
    return match.image;
  }

  const categoryFallback = getCategoryFallback(category);
  return categoryFallback || IMAGE_FALLBACKS.scenic;
}
